<script>
export default {
    data(){
        return{
            current: 0,
            speed: 10000,
            timer: null,
            slides : [{
                image : 'img/hero-1.jpg',
                spanTest : "Bag,kids",
                headTest : "Black friday",
                caption : "Lorem ipsum dolor sit amet, consectetur adipisicing elit, sed do eiusmod tempor incididunt ut labore et dolore"
            },
            {image : 'img/hero-2.jpg',
                spanTest : "Bag,kids",
                headTest : "Black friday",
                caption : "Lorem ipsum dolor sit amet, consectetur adipisicing elit, sed do eiusmod tempor incididunt ut labore et dolore"
            }]
        }
    },
     methods: {
    startRotation: function () {
      this.timer = setInterval(this.next, this.speed);
    },
    stopRotation: function () {
      clearTimeout(this.timer);
      this.timer = null;
    },
    next: function () {
      var current = this.current;
      var next = current + 1;

      if (next > this.slides.length - 1) {
        next = 0;
      }
      this.current = next;
      this.setActive(this.current);
    },
    prev: function () {
      var current = this.current;
      var prev = current - 1;

      if (prev < 0) {
        prev = this.slides.length -1;
      }

      this.current = prev;
      this.setActive(this.current);
    },
    isActive: function (slide) {
      return this.current === slide;
    },
    setActive: function (slide) {
      this.current = slide;
    },
  },
  mounted(){
      this.startRotation();
  },
  async created(){
      try {
        await this.$parent.getProfile()
      } catch (error) {
          
      }
  }
}

